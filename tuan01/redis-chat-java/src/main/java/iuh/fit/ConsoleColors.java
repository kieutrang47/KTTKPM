package iuh.fit;

public class ConsoleColors {
    public static final String RESET = "\033[0m";  // Reset màu về mặc định
    public static final String RED = "\033[0;31m";    // Màu đỏ
    public static final String GREEN = "\033[0;32m";  // Màu xanh lá
    public static final String CYAN = "\033[0;36m";   // Màu xanh dương nhạt
    public static final String YELLOW = "\033[0;33m"; // Màu vàng
    public static final String PURPLE_BOLD = "\033[1;35m"; // Tím đậm
}